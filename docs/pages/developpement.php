<h3>Remarques</h3>
<div class="text">
<p>
    Le code de <b>Formidable</b> a été remanié et doit être encore amélioré. <b>Formidable</b> embarque son propre parser caractère par caractère, ce qui est intentionnel, même si le code pourrait être plus léger en utilisant <b>DOM</b> ou une autre bibliothèque, les performances s'en trouveraient très probablement amoindries.
</p>
<p>
    La philosophie de <b>Formidable</b> est de garder une structure simple et d'être utilisé dans des petits projets rapides.
</p>
<p>
    <b>Formidable</b> possède actuellement des messages d'erreurs en français uniquement (codées en dur). La bibliothèque pourrait cependant facilement être internationalisée si le besoin s'en faisait ressentir, mais ce n'est pas pour le moment au programme. Notez que le code source en lui même est en anglais.
</p>
</div>
